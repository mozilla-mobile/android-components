package org.mozilla.samples.browser.integration

import android.content.Context
import mozilla.components.browser.session.SessionManager
import mozilla.components.concept.engine.Engine
import mozilla.components.feature.accounts.WebChannelFeature
import mozilla.components.support.base.feature.LifecycleAwareFeature

class WebChannelIntegration(
    context: Context,
    engine: Engine,
    sessionManager: SessionManager
) : LifecycleAwareFeature {

    private val feature = WebChannelFeature(context, engine, sessionManager)

    override fun start() {
        feature.start()
    }

    override fun stop() {
        feature.stop()
    }
}
